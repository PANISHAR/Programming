//check the size of data type...
#include<bits/stdc++.h>
using namespace std;
int main()
{
	int a=10;
	cout<<"size of int:"<<sizeof(a)<<endl;
	float p=3.1415;
	cout<<"size of float:"<<sizeof(p)<<endl;
	double d = 222.8282;
	cout<<"size of double:"<<sizeof(d)<<endl;
	char ch = 'q';
	cout<<"size of char:"<<sizeof(ch)<<endl;
	long l = 10000;
	cout<<"size of long int:"<<sizeof(l)<<endl;
	short s = 21;
	cout<<"size of short int:"<<sizeof(s)<<endl;
}